# 6. Спортсмен занимается ежедневными пробежками. В первый день его результат составил a километров.
#   Каждый день спортсмен увеличивал результат на 10 % относительно предыдущего.
#   Требуется определить номер дня, на который общий результат спортсмена составить не менее b километров.
#   Программа должна принимать значения параметров a и b и выводить одно натуральное число — номер дня.
#   Например: a = 2, b = 3.
#   Результат:
#   1-й день: 2
#   2-й день: 2,2
#   3-й день: 2,42
#   4-й день: 2,66
#   5-й день: 2,93
#   6-й день: 3,22
#   Ответ: на 6-й день спортсмен достиг результата — не менее 3 км."

a = float(input("И крайнее задание на сегодня. Введи сколько спортсмен пробежал в 1 день? "))
b = float(input("А сколько надо пробегать? "))
if a > b:
    print("Спортсмен уже достаточно натренирован чтобы пробегать %.2f км в день." % b)
else:
    print("1-й день, %.2f км, цель %.2f км" % (a, b))
    daycount = 1
    while b > a:
        a = a/100*10+a
        daycount += 1
        print("%d-й день, %.2f км, цель %.2f км" % (daycount, a, b))
print("На %d-й день спортсмен достиг результата — не менее %.2f км." % (daycount, b))