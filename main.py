    bigchislonew = chislo // (10 ** (chislostr - 1))
    chislo -= 10 ** (chislostr - 1) * bigchislonew
    # print("bigchislonew = ", bigchislonew)
    # print("chislo = ", chislo)
    if bigchislonew > bigchislo:
        bigchislo = bigchislonew
        # print("bigchislo = ", bigchislo)
        if bigchislo == 9: break
    chislostr -= 1
